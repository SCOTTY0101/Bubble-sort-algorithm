
/*Javascript Bubble sort algorithm. Swaps adjacent elements if they are in the wrong order
* Time complexity O(n^2)
*/ 

let bubble = arr => {
    for (let i = 0; i < arr.length; i++) {
        for (let j = 0; j < arr.length - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]]
            }
        }
    }
    return arr;
}